const backgroundColorType = {
    success: 'success',
    error: 'danger',
    warning: 'warning',
    info: 'info'
};
export function showToast(message, type = "primary", timeout = 3000) {
    let toastContainer = document.getElementById("global-toast-container");
    let toastDiv = document.createElement("div");
    let bgType = backgroundColorType[type];
    toastDiv.className = `toast align-items-center text-bg-${bgType} border-0 show`;
    toastDiv.role = "alert";
    toastDiv.setAttribute("aria-live", "assertive");
    toastDiv.setAttribute("aria-atomic", "true");

    toastDiv.innerHTML = `
        <div class="d-flex">
            <div class="toast-body d-flex align-items-center">
                <div class="toast-icon">${icons[type] || icons["info"]}</div>
                <div class="toast-message">${message}</div>
            </div>
            <button type="button" class="btn-close me-2 m-auto" data-bs-dismiss="toast" aria-label="Close"></button>
        </div>
    `;

    toastContainer.appendChild(toastDiv);

    // Tự động ẩn toast sau timeout
    setTimeout(() => {
        toastDiv.classList.remove("show");
        setTimeout(() => toastDiv.remove(), 500);
    }, timeout);
}

export function initToastFromSession() {
    let flashMessage = document.body.getAttribute("data-flash-message");
    let flashType = document.body.getAttribute("data-flash-type") || "success";

    if (flashMessage) {
        showToast(flashMessage, flashType);
    }
}
